export function FeatureReveal() {
  const sectionRef = useRef<HTMLDivElement>(null);
  const [isVisible, setIsVisible] = useState(false);

  useEffect(() => {
    const el = sectionRef.current;
    if (!el) return;

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setIsVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.15 }
    );

    observer.observe(el);
    return () => observer.disconnect();
  }, []);

  return (
    <div ref={sectionRef} className="grid grid-cols-1 gap-3 sm:grid-cols-2 lg:grid-cols-5 lg:gap-4">
      {FEATURES.map((feature, i) => (
        <div
          key={feature.title}
          className={`group rounded-sm border border-white/5 bg-primary p-5 transition-all duration-700 ease-out hover:border-secondary/40 hover:bg-primary/80 lg:p-6 ${
            isVisible
              ? "opacity-100 translate-x-0 translate-y-0"
              : "opacity-0 -translate-x-8 translate-y-4"
          }`}
          style={{ transitionDelay: isVisible ? `${i * 120}ms` : "0ms" }}
        >
          <div className="mb-3 inline-flex rounded-sm bg-secondary/10 p-2.5 text-secondary transition-colors group-hover:bg-secondary/20">
            {feature.icon}
          </div>
          <h3 className="font-sans text-sm font-bold text-foreground transition-colors group-hover:text-secondary lg:text-base">
            {feature.title}
          </h3>
          <p className="mt-2 text-xs leading-relaxed text-foreground/60 lg:text-sm">
            {feature.description}
          </p>
        </div>
      ))}
    </div>
  );
}
